package model;

import java.util.Date;

public class Seqs {

    private int seqsNo;
    private String seqsTp;
    private Date seqsDt;

    public int getSeqsNo() {
        return seqsNo;
    }

    public void setSeqsNo(int seqsNo) {
        this.seqsNo = seqsNo;
    }

    public String getSeqsTp() {
        return seqsTp;
    }

    public void setSeqsTp(String seqsTp) {
        this.seqsTp = seqsTp;
    }

    public Date getSeqsDt() {
        return seqsDt;
    }

    public void setSeqsDt(Date seqsDt) {
        this.seqsDt = seqsDt;
    }

}
